        <p class="text-stone-600">
            本規約は、ユーザーと当サービス運営者との間の、当サービスの利用に関わる一切の関係に適用されます。
        </p>

        <hr class="my-8 border-stone-200">

        <h2 class="text-xl font-bold text-stone-800 mt-8 mb-4">第2条（利用登録）</h2>
        <ol class="list-decimal list-inside text-stone-600 space-y-2">
            <li>当サービスの利用を希望する者は、当サービスの定める方法により利用登録を行うものとします。</li>
            <li>運営者は、以下の場合、利用登録を承認しないことがあります。
                <ul class="list-disc list-inside ml-4 mt-2 space-y-1">
                    <li>登録内容に虚偽、誤記、記入漏れがあった場合</li>
                    <li>過去に本規約違反があった場合</li>
                    <li>その他、運営者が不適切と判断した場合</li>
                </ul>
            </li>
        </ol>

        <hr class="my-8 border-stone-200">

        <h2 class="text-xl font-bold text-stone-800 mt-8 mb-4">第3条（アカウント管理）</h2>
        <ol class="list-decimal list-inside text-stone-600 space-y-2">
            <li>ユーザーは、自己の責任においてアカウント情報を管理するものとします。</li>
            <li>アカウント情報の第三者への譲渡、貸与、共有は禁止します。</li>
            <li>アカウントの不正使用によって生じた損害について、運営者は一切の責任を負いません。</li>
        </ol>

        <hr class="my-8 border-stone-200">

        <h2 class="text-xl font-bold text-stone-800 mt-8 mb-4">第4条（禁止事項）</h2>
        <p class="text-stone-600 mb-2">ユーザーは、以下の行為をしてはなりません。</p>
        <ul class="list-disc list-inside text-stone-600 space-y-1">
            <li>法令または公序良俗に違反する行為</li>
            <li>他のユーザーまたは第三者の権利・利益を侵害する行為</li>
            <li>不正アクセス、またはこれを試みる行為</li>
            <li>当サービスの運営を妨害する行為</li>
            <li>虚偽の情報を登録・投稿する行為</li>
            <li>その他、運営者が不適切と判断する行為</li>
        </ul>

        <hr class="my-8 border-stone-200">

        <h2 class="text-xl font-bold text-stone-800 mt-8 mb-4">第5条（サービスの提供停止・変更）</h2>
        <p class="text-stone-600 mb-2">運営者は、以下の場合、事前の通知なく当サービスの全部または一部を停止・変更できるものとします。</p>
        <ul class="list-disc list-inside text-stone-600 space-y-1">
            <li>システム保守、障害対応を行う場合</li>
            <li>天災、停電、通信障害等の不可抗力が生じた場合</li>
            <li>その他、運営者が必要と判断した場合</li>
        </ul>

        <hr class="my-8 border-stone-200">

        <h2 class="text-xl font-bold text-stone-800 mt-8 mb-4">第6条（退会）</h2>
        <p class="text-stone-600">
            ユーザーは、当サービスの定める方法により、いつでも退会することができます。<br>
            退会後、ユーザー情報は、法令または運営上必要な場合を除き、適切に削除されます。
        </p>

        <hr class="my-8 border-stone-200">

        <h2 class="text-xl font-bold text-stone-800 mt-8 mb-4">第7条（免責事項）</h2>
        <ol class="list-decimal list-inside text-stone-600 space-y-2">
