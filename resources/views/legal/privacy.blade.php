    <article class="prose prose-stone max-w-none">
        <h1 class="text-3xl font-bold text-stone-800 mb-6">プライバシーポリシー</h1>

        <p class="text-stone-600 mb-8">
            本プライバシーポリシー（以下「本ポリシー」）は、本サービス（以下「当サービス」）における、ユーザーの個人情報の取扱いについて定めるものです。
        </p>

        <hr class="my-8 border-stone-200">

        <h2 class="text-xl font-bold text-stone-800 mt-8 mb-4">1. 取得する情報</h2>
        <p class="text-stone-600 mb-2">当サービスは、以下の情報を取得する場合があります。</p>
        <ul class="list-disc list-inside text-stone-600 space-y-1">
            <li>氏名、ユーザー名</li>
            <li>メールアドレス</li>
            <li>パスワード（暗号化された状態で保存されます）</li>
            <li>プロフィール情報（アイコン画像等、ユーザーが任意で登録する情報）</li>
            <li>サービス利用履歴、操作ログ</li>
            <li>その他、ユーザーが入力・送信する情報</li>
        </ul>

        <hr class="my-8 border-stone-200">

        <h2 class="text-xl font-bold text-stone-800 mt-8 mb-4">2. 個人情報の利用目的</h2>
        <p class="text-stone-600 mb-2">当サービスは、取得した個人情報を以下の目的で利用します。</p>
        <ul class="list-disc list-inside text-stone-600 space-y-1">
            <li>当サービスの提供・運営のため</li>
            <li>ユーザー認証および本人確認のため</li>
            <li>ユーザーからのお問い合わせ対応のため</li>
            <li>サービス改善・機能向上のため</li>
            <li>不正利用の防止およびセキュリティ確保のため</li>
        </ul>

        <hr class="my-8 border-stone-200">

        <h2 class="text-xl font-bold text-stone-800 mt-8 mb-4">3. 個人情報の管理</h2>
        <p class="text-stone-600">
            当サービスは、個人情報への不正アクセス、漏えい、改ざん、紛失を防止するため、適切な安全管理措置を講じます。
        </p>

        <hr class="my-8 border-stone-200">

        <h2 class="text-xl font-bold text-stone-800 mt-8 mb-4">4. 個人情報の第三者提供</h2>
        <p class="text-stone-600 mb-2">当サービスは、以下の場合を除き、個人情報を第三者に提供しません。</p>
        <ul class="list-disc list-inside text-stone-600 space-y-1">
            <li>ユーザー本人の同意がある場合</li>
            <li>法令に基づく場合</li>
            <li>人の生命、身体または財産の保護のために必要がある場合で、本人の同意を得ることが困難な場合</li>
        </ul>

        <hr class="my-8 border-stone-200">

        <h2 class="text-xl font-bold text-stone-800 mt-8 mb-4">5. 外部サービスの利用</h2>
        <p class="text-stone-600 mb-2">当サービスは、以下の外部サービスを利用する場合があります。</p>
        <ul class="list-disc list-inside text-stone-600 space-y-1">
            <li>クラウドストレージサービス（例：画像保存のための外部ストレージ）</li>
            <li>認証サービス（例：Google ログイン）</li>
        </ul>
        <p class="text-stone-600 mt-2">
            これらの外部サービスにおける個人情報の取扱いについては、各サービス提供者のプライバシーポリシーをご確認ください。
        </p>

        <hr class="my-8 border-stone-200">

        <h2 class="text-xl font-bold text-stone-800 mt-8 mb-4">6. 個人情報の開示・訂正・削除</h2>
        <p class="text-stone-600">
            ユーザーは、自己の個人情報について、開示、訂正、削除を求めることができます。<br>
            これらの請求は、当サービスの定める方法により受け付けます。
        </p>

        <hr class="my-8 border-stone-200">

        <h2 class="text-xl font-bold text-stone-800 mt-8 mb-4">7. アクセス解析・ログについて</h2>
        <p class="text-stone-600">
            当サービスは、サービス品質向上および不正防止のため、アクセスログや操作履歴を記録する場合があります。
        </p>

        <hr class="my-8 border-stone-200">

        <h2 class="text-xl font-bold text-stone-800 mt-8 mb-4">8. プライバシーポリシーの変更</h2>
        <p class="text-stone-600">
            当サービスは、法令の変更やサービス内容の改善に伴い、本ポリシーを変更することがあります。<br>
            変更後のプライバシーポリシーは、当サービス上に掲載した時点で効力を生じます。
        </p>

        <hr class="my-8 border-stone-200">

        <h2 class="text-xl font-bold text-stone-800 mt-8 mb-4">9. お問い合わせ</h2>
        <p class="text-stone-600">
            個人情報の取扱いに関するお問い合わせは、当サービスのお問い合わせ窓口までご連絡ください。
        </p>

